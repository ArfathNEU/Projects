// Build a reverse lookup by IATA code
const IATA_TO_ICAO: Record<string, string> = {}
for (const [icao, ap] of Object.entries(AIRPORTS)) {
  if (ap.iata) IATA_TO_ICAO[ap.iata] = icao
}

export function lookupByIcao(icao: string): Airport | null {
  return AIRPORTS[icao.toUpperCase()] ?? null
}

export function lookupByIata(iata: string): Airport | null {
  const icao = IATA_TO_ICAO[iata.toUpperCase()]
  return icao ? AIRPORTS[icao] : null
}

export function lookupAirport(code: string): Airport | null {
  const upper = code.toUpperCase()
  return AIRPORTS[upper] ?? lookupByIata(upper)
}
